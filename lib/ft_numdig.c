#include "lib.h"

int	ft_numdig(int n, int base)
{
	int len;

	len = (n < 0) ? 1 : 0;
	if (n == 0)
		return (1);
	while (n != 0)
	{
		n = n / base;
		len++;
	}
	return (len);
}
